    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblEmployee.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to Update/View");
            return;
        }
        DefaultTableModel Employee = (DefaultTableModel) tblEmployee.getModel();
        Employee selectedProfile = (Employee) Employee.getValueAt(selectedRowIndex, 0);
        txtName.setText(selectedProfile.getName());
        txtEmployeeId.setText(String.valueOf(selectedProfile.getEmployeeId()));
        txtAge.setText(String.valueOf(selectedProfile.getAge()));
        txtGender.setText(selectedProfile.getGender());
        txtStartDate.setText(selectedProfile.getStartDate());
        txtLevel.setText(selectedProfile.getLevel());
        txtTeamInfo.setText(selectedProfile.getTeamInfo());
        txtPositionTitle.setText(selectedProfile.getPositionTitle());
        txtCellPhoneNumber.setText(String.valueOf(selectedProfile.getCellPhoneNumber()));
        txtEmailAddress.setText(selectedProfile.getEmailAddress());
        
        ImageIcon imageIcon = new ImageIcon(selectedProfile.getPhoto());
        if(imageIcon != null){
            Image img = imageIcon.getImage();
            Image newimg = img.getScaledInstance(150,150,java.awt.Image.SCALE_SMOOTH);
            //Image newimg = img.getScaledInstance(lbl_pic.getWidth(), lbl_pic.getHeight(),  java.awt.Image.SCALE_SMOOTH);
            imageIcon = new ImageIcon(newimg);
            lblPhotoArea.setIcon(imageIcon);
            lblPhotoArea.setText(null);
        }
        else{
            lblPhotoArea.setText("Image not found");
        }
        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblEmployee.getSelectedRow();
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        DefaultTableModel Employee = (DefaultTableModel) tblEmployee.getModel();
        Employee selectedProfile = (Employee) Employee.getValueAt(selectedRowIndex, 0);
        employeHistory.deleteEmployee(selectedProfile);
        JOptionPane.showMessageDialog(this, "Profile deleted");
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


