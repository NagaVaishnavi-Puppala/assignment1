    private void populateTable() {

        DefaultTableModel model = (DefaultTableModel)tblEmployee.getModel();
        model.setRowCount(0);
        for(Employee profile : employeHistory.getEmployeHistory()){
             Object[] row = new Object[4];
            row[0] = profile;
            row[1] = profile.getEmployeeId();
            row[2] = profile.getTeamInfo();
            row[3] = profile.getPositionTitle();
            model.addRow(row); 
        }

    }
